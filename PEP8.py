"""
O que são PEP'S?
 - São arquivos que apresentam propostas de melhoria para a Linguagem Python
 - https://www.python.org/dev/peps/
 
 Regras mais utilizadas:
 1) O pycharm vem com padrão de reconhecimento de palavras em inglês.
  - Para o pycharm reconhecer palavras em português faça:
    File - settings - Inspections - Proofreading - Desmarque Typo
    
 2) Identação (Organizar código):
  - Utiliza 4 espaços para cada código dentro de blocos como funções, condicionais, entre outros.
# EX (Condicionais):
if x <10:
    if y > 10:
        y = 5
Obs: Sempre utilize 4 espaços ao invés da tecla TAB.

 3) Linhas em branco:
  - Seu programa deve ter sempre uma linha em branco no final.
  - Separe funções e classes com 2 linhas em branco.
Ex:
def x():
    pass


def y():
    pass

 4) Importação:
  - Se você deseja fazer importação de duas bibliotecas, importe uma por vez e logo no
  início do programa (Após os comentários iniciais e antes da declaração da primeira (variavel/função)
Ex:
Modo certo:
import x
import y

Modo errado:
import x, y

 5) Uso de espaços:
  - Nunca utilize espaços antes ou depois de chaves, colchetes ou parenteses.
  - Use um espaço antes e depois quando declarar uma variável ou usar um operador do tipo <, >, ==, entre outros.
Ex:
def x():
    pass

y = 18

 6) Nomenclatura de variáveis:
  - Utilizar letras minúsculas separadas por underline(_)
  - Utilizar letras maiúsculas separadas por underline(_)
  - Utilizar palavars começando com letras maiúsculas
Ex:
idadeJoao = 17
idade_joao = 17
IDADE_JOAO = 17
"""
